function App() {
  const [accountSelected, setAccountSelected] = useState(null)
  const [connected, setConnected] = useState(false)
  const [amount, setAmount] = useState(1)
  const [supply, setSupply] = useState(0)
  const [contract, setContract] = useState(null)

  useEffect(() => {
    const web3 = new Web3(window.ethereum)
    const _contract = new web3.eth.Contract(ABI, CONTRACT_ADDRESS)
    getSupply(_contract)
    setContract(_contract)
  }, [connected])

  const getSupply = async (_contract) => {
    const supplyAmount = await _contract.methods.totalSupply().call()
    setSupply(supplyAmount)
  }

  return (
    <div
      style={{
        background: `url(${Background})`,
        backgroundRepeat: 'no-repeat',
        backgroundSize: 'cover',
        padding: '20px 40px 0px',
        height: 'calc(100vh - 20px)',
        display: 'flex',
        flexDirection: 'column',
      }}
    >
      <div>
        <Header
          connected={connected}
          setConnected={setConnected}
          accountSelected={accountSelected}
          setAccountSelected={setAccountSelected}
        />
      </div>
      <div
        style={{
          display: 'flex',
          height: '100%',
        }}
      >
        <div id="container" style={{ flexBasis: '50%', marginTop: '20px' }}>
          <Minter
            amount={amount}
            setAmount={setAmount}
            connected={connected}
            MAX_AMOUNT={MAX_AMOUNT}
            supply={supply}
            MAX_SUPPLY={MAX_SUPPLY}
            contract={contract}
            accountSelected={accountSelected}
            BASE_PRICE={BASE_PRICE}
          />
        </div>
        <div
          id="hajje"
          style={{ flexBasis: '50%', alignSelf: 'flex-end', display: 'flex' }}
        >
          <img
            style={{
              objectFit: 'contain',
              width: '100%',
              height: '100%',
              maxHeight: '700px',
            }}
            src={Hajje}
            alt=""
          />
        </div>
      </div>
    </div>
  )
}

export default App
